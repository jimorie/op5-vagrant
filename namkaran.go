func main() {

	diabloPtr := flag.String("d", "", "Returns a random (d)iablo item name. Requires input without spaces.\nExample: 'prefix-INPUT-of-suffix'\n")
	shipPtr := flag.Bool("s", false, "Return a random British navy (s)hip. Takes no input.\n")
	flag.Parse()

	s1 := rand.NewSource(time.Now().UnixNano())
	r1 := rand.New(s1)

	if *shipPtr {
		// print random ship name in lowercase
		fmt.Println(strings.ToLower(britishNavyShips[r1.Intn(len(britishNavyShips)-1)]))
	} else if *diabloPtr != "" {
		// take in input and place it between prefix and suffix
		prefix := strings.ToLower(diabloPrefix[r1.Intn(len(diabloPrefix)-1)])
		suffix := strings.ToLower(diabloSuffix[r1.Intn(len(diabloSuffix)-1)])
		fmt.Printf("%v-%v-of-%v\n", prefix, *diabloPtr, suffix)
	} else {
		fmt.Println("\n\tnamkaran\n\n\tFrom sanskrit नाम (nāma, “name”) +‎ करण (karaṇa, “making”).")
		fmt.Println("\tA program to generate interesting, human-readable names.\n\tUse one of the flags below to generate desired output.\n")
		flag.PrintDefaults()
	}
}

